#include "PlayerScore.h"

PlayerScore::PlayerScore() :
	WinPID(-1)
{
}

PlayerScore::~PlayerScore()
{
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// 勝利者設定
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
void PlayerScore::SetWinPID(int PID)
{
	WinPID = PID;
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// 勝利者受け取り
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
int PlayerScore::GetWinPID()
{
	return WinPID;
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// スコア設定
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
void PlayerScore::SetScore(int pID, Score score)
{
	switch (pID)
	{
	case 0:
		p1Score = score;
		break;
	case 1:
		p2Score = score;
		break;
	}
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// スコア受渡
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
Score PlayerScore::GetScore(int pID)
{
	switch (pID)
	{
	case 0:
		return p1Score;
	case 1:
		return p2Score;
	}
	return Score();
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// ステータス設定
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
void PlayerScore::SetStatas(int pID, Statas statas)
{
	switch (pID)
	{
	case 0:
		p1Statas = statas;
		break;
	case 1:
		p2Statas = statas;
		break;
	}
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// ステータス受渡
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
Statas PlayerScore::GetStatas(int pID)
{
	switch (pID)
	{
	case 0:
		return p1Statas;
	case 1:
		return p2Statas;
	}
	return Statas();
}
