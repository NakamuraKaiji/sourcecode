//----------------------------------------------------------------------------------------------------
//	BaseScene
//			各シーンの元となる継承元クラス
// 
//----------------------------------------------------------------------------------------------------
#include <DxLib.h>
#include "BaseScene.h"

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// コンストラクタ
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
BaseScene::BaseScene() :
	screenWidth(0),
	screenHeight(0),
	system(nullptr),
	fdata(nullptr),
	tdata(nullptr),
	sdata(nullptr),
	score(nullptr),
	changeSceneID(SceneID::None)
{
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// デストラクタ
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
BaseScene::~BaseScene()
{
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// シーン切り替え処理
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+	
void BaseScene::ChangeScene(SceneID sceneID)
{
	changeSceneID = sceneID;
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// 基本的な初期化
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+	
void BaseScene::BaseInitialize(int screenWidth, int screenHeight, GameSystemObjects* system, FontHandle* fdata, TextureManger* tdata, Sound* sdata, PlayerScore* score)
{
	this->screenWidth = screenWidth;
	this->screenHeight = screenHeight;
	this->system = system;
	this->fdata = fdata;
	this->tdata = tdata;
	this->sdata = sdata;
	this->score = score;
	changeSceneID = SceneID::None;

	//	各シーンの初期化
	Initialize();
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// 基本的な解放処理
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
void BaseScene::BaseFinalize()
{
	Finalize();
}

//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+
// どのシーンに遷移するかの受渡
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------+	
SceneID BaseScene::GetChangeSceneID()
{
	return changeSceneID;
}
